# ● Create a variable x and give it the value 0.
x = 0

# ● Use a while loop to print the value of x in every iteration 
# of the loop. After printing, the value of x should increase by 1. The loop should run as long as x 
# is smaller than or equal to 10.

n = 10

while x <= n:
    print(x)
    x = x + 1
